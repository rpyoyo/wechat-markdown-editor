// Get theme CSS by name
export function getCodeThemeCSS(themeName: string): string {
  const themes: Record<string, string> = {
    'github': github,
    'github-dark': githubDark,
    'vs2015': vs2015,
    'atom-one-dark': atomOneDark,
  }
  return themes[themeName] || githubDark // Default to github-dark
}
